package com.CoreJava;

public class TwoDArray {

	public static void main(String[] args) {
		
		int[][] Array = { {12, 22},{45, 65}};

		System.out.println("Element at Array[0][0] = " + Array[0][0]);
		System.out.println("Element at Array[0][1] = " + Array[0][1]);
		System.out.println("Element at Array[1][0] = " + Array[1][0]);
		System.out.println("Element at Array[1][1] = " + Array[1][1]);

	}

}
